"""Словарь CVM-терминов для корректной классификации полей брифа.

Используется brief analyzer'ом, чтобы:
- НЕ путать бизнес-цели («апсейл», «удержание», «активация») с аудиторией;
- быстрее определять product/goal/audience-токены без LLM;
- подсказывать LLM правильные категории через примеры в few-shot.

Все списки — нормализованные substring-фрагменты (rus + en основы).
"""

from __future__ import annotations

# Бизнес-цели CVM кампаний. После «для апсейла», «цель — удержание» — это GOAL.
GOAL_TERMS: tuple[str, ...] = (
    "апсейл", "апсэйл", "ап-сейл", "ап сейл", "upsell", "up-sell",
    "кросс-сейл", "кросс сейл", "крос-сейл", "крос сейл", "cross-sell", "cross sell", "перекрёстн прода", "перекрестн прода",
    "удержани", "retention",
    "реактивац", "reactivat", "возврат клиент",
    "активаци продукт", "активаци услуг", "activation",
    "первичн прода", "первая прода",
    "продаж", "продажа", "продажу", "продвижени",
    "онбординг", "onboarding",
    "лояльност", "loyalty",
    "монетизац",
    "churn", "отток клиент",
    "конверси",
    "повышени arpu", "рост arpu",
    "стимулирован", "промоушн", "promo",
)

# Признаки того, что захваченный текст описывает аудиторию (а не цель/продукт).
AUDIENCE_HINTS: tuple[str, ...] = (
    "клиент", "абонент", "пользоват", "юзер",
    "семь", "семейн",  "молодёж", "молодеж", "подрост", "пенсион", "ветеран",
    "активн", "неактивн", "новые", "новички", "лояльн",
    "корпорат", "b2b", "b2c",
    "отток", "churn",
    "сегмент", "аудитор", "таргет групп", "target group",
    "регион", "город",
    "тариф", "premium", "vip",
)

# Признаки того, что захваченный текст описывает продукт/услугу.
PRODUCT_TYPE_HINTS: tuple[str, ...] = (
    "тариф", "пакет", "опци", "услуг", "продукт", "подписк", "сервис",
    "premium", "lite", "smart",
    "мб", "гб", "минут", "sms-пакет",
    "интернет", "роуминг",
)


def is_goal_phrase(text: str) -> bool:
    """True если строка похожа на бизнес-цель CVM."""
    if not text:
        return False
    lower = text.lower()
    return any(term in lower for term in GOAL_TERMS)


def looks_like_audience(text: str) -> bool:
    """True если строка похожа на описание аудитории."""
    if not text:
        return False
    lower = text.lower()
    return any(hint in lower for hint in AUDIENCE_HINTS)


def looks_like_product(text: str) -> bool:
    """True если строка похожа на продукт/тариф."""
    if not text:
        return False
    lower = text.lower()
    return any(hint in lower for hint in PRODUCT_TYPE_HINTS)


